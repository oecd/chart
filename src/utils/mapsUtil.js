export const getDottedMapLines = (countrycodes) => {
  const relevantDottedBorders = R.reject(
    R.compose(R.isEmpty, R.intersection(countrycodes), R.prop('countryCodes')),
    dottedBorders,
  );

  return R.map(
    ({ coordinates }) => ({
      type: 'mapline',
      lineWidth: 1.5,
      dashStyle: 'dot',
      enableMouseTracking: false,
      data: [
        {
          color: 'red',
          geometry: {
            type: 'MultiLineString',
            coordinates,
          },
        },
      ],
    }),
    relevantDottedBorders,
  );
};
